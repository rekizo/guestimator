// Returns { sunrise, sunset } in minutes from midnight local time (TAHT = UTC−10).
// passLat / passLon use the app's convention: positive south, positive west.
function getSunTimes(date, passLat, passLon) {
  const UTC_OFFSET = -10 * 60; // TAHT = UTC−10 in minutes
  // Standard astronomical convention: lat positive north, lon positive east
  const lat = -passLat;
  const lon = -passLon;

  // Julian day for noon UTC on this date
  const JD = (Date.UTC(date.getFullYear(), date.getMonth(), date.getDate(), 12) / 86400000) + 2440587.5;
  const T  = (JD - 2451545.0) / 36525.0;

  // Geometric mean longitude + anomaly
  const L0    = (280.46646 + T * (36000.76983 + T * 0.0003032)) % 360;
  const M     = 357.52911 + T * (35999.05029 - T * 0.0001537);
  const Mrad  = M * Math.PI / 180;

  // Equation of center → true longitude → apparent longitude
  const C      = Math.sin(Mrad) * (1.914602 - T * (0.004817 + 0.000014 * T))
               + Math.sin(2 * Mrad) * (0.019993 - 0.000101 * T)
               + Math.sin(3 * Mrad) * 0.000289;
  const omega  = 125.04 - 1934.136 * T;
  const lambda = (L0 + C - 0.00569 - 0.00478 * Math.sin(omega * Math.PI / 180)) * Math.PI / 180;

  // Obliquity → declination
  const eps0   = (23.0 + (26.0 + (21.448 - T * (46.815 + T * (0.00059 - T * 0.001813))) / 60) / 60) * Math.PI / 180;
  const eps    = eps0 + 0.00256 * Math.cos(omega * Math.PI / 180) * Math.PI / 180;
  const sinDec = Math.sin(eps) * Math.sin(lambda);
  const dec    = Math.asin(sinDec);

  // Equation of time (minutes)
  const y   = Math.tan(eps / 2) ** 2;
  const L0r = L0 * Math.PI / 180;
  const e   = 0.016708634;
  const eot = 4 * (180 / Math.PI) * (
    y * Math.sin(2 * L0r) - 2 * e * Math.sin(Mrad)
    + 4 * e * y * Math.sin(Mrad) * Math.cos(2 * L0r)
    - 0.5 * y * y * Math.sin(4 * L0r) - 1.25 * e * e * Math.sin(2 * Mrad)
  );

  // Hour angle (degrees) for standard sunrise/sunset (centre of sun, −0.833° for refraction + disc)
  const latRad = lat * Math.PI / 180;
  const cosHa  = (Math.cos(90.833 * Math.PI / 180) - Math.sin(latRad) * sinDec)
               / (Math.cos(latRad) * Math.cos(dec));
  if (Math.abs(cosHa) > 1) return { sunrise: 0, sunset: 1440 }; // polar edge case

  const Ha = Math.acos(Math.max(-1, Math.min(1, cosHa))) * 180 / Math.PI;

  // Solar noon UTC (minutes from midnight)
  const noonUTC = 720 - 4 * lon - eot;
  const riseUTC = noonUTC - 4 * Ha;
  const setUTC  = noonUTC + 4 * Ha;

  // Convert to local minutes-from-midnight, clamped to [0, 1440)
  const toLocal = (utcMin) => ((Math.round(utcMin) + UTC_OFFSET) % 1440 + 1440) % 1440;
  return { sunrise: toLocal(riseUTC), sunset: toLocal(setUTC) };
}

