function CompassRose({ facing }) {
  // Convert "NNW", "SSW" etc. to a bearing in degrees
  const dir16 = { N:0, NNE:22.5, NE:45, ENE:67.5, E:90, ESE:112.5, SE:135, SSE:157.5,
                  S:180, SSW:202.5, SW:225, WSW:247.5, W:270, WNW:292.5, NW:315, NNW:337.5 };
  const primary = facing.split("-")[0].split("/")[0].trim();
  const deg = dir16[primary] != null ? dir16[primary] : 0;
  // Arrow points in the direction water exits (outgoing direction = pass facing)
  return (
    <svg viewBox="0 0 80 80" width={80} height={80}>
      <circle cx={40} cy={40} r={36} fill="none" stroke="#d6cfc4" strokeWidth={1.5}/>
      <circle cx={40} cy={40} r={2} fill="#8a7e6e"/>
      {["N","E","S","W"].map((d,i) => {
        const a = i * 90 * Math.PI / 180;
        const x = 40 + 30 * Math.sin(a), y = 40 - 30 * Math.cos(a);
        return <text key={d} x={x} y={y} textAnchor="middle" dominantBaseline="middle"
          fontSize={9} fill="#8a7e6e" fontFamily="sans-serif">{d}</text>;
      })}
      {/* Arrow in "facing" direction */}
      <g transform={`rotate(${deg}, 40, 40)`}>
        <polygon points="40,10 36,30 40,26 44,30" fill="#e07b39" opacity={0.9}/>
        <polygon points="40,70 36,50 40,54 44,50" fill="#d6cfc4" opacity={0.7}/>
      </g>
      <text x={40} y={74} textAnchor="middle" fontSize={7} fill="#8a7e6e" fontFamily="sans-serif">{facing}</text>
    </svg>
  );
}

