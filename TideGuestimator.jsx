// ─── ALGORITHM ───────────────────────────────────────────────────────────────

function toDateStr(d) {
  return `${d.getFullYear()}-${String(d.getMonth()+1).padStart(2,"0")}-${String(d.getDate()).padStart(2,"0")}`;
}

function shiftDate(d, days) {
  const nd = new Date(d); nd.setDate(nd.getDate() + days); return nd;
}

/** Gather and label tide events across a multi-day window */
function getExpandedTides(date, pass) {
  // Step 1: collect raw events
  const raw = [];
  [-1, 0, 1, 2].forEach(offset => {
    const dayData = TIDE_DATA[toDateStr(shiftDate(date, offset))];
    if (!dayData) return;
    dayData.forEach(([tMin, h]) =>
      raw.push({ time: tMin + offset * 1440 + pass.rangiOffset, height: h })
    );
  });
  raw.sort((a, b) => a.time - b.time);

  // Step 2: deduplicate parsing artifacts (events within 25 min AND 0.3 ft of each other)
  const deduped = [];
  for (const ev of raw) {
    const prev = deduped[deduped.length - 1];
    if (prev && Math.abs(ev.time - prev.time) < 25 && Math.abs(ev.height - prev.height) < 0.3) {
      // Keep the one with the more extreme height (max of abs heights for H candidates)
      if (Math.abs(ev.height) > Math.abs(prev.height)) deduped[deduped.length - 1] = ev;
      continue;
    }
    deduped.push(ev);
  }

  // Step 3: label each event using local max/min
  const labeled = deduped.map((ev, i) => {
    const prev = deduped[i - 1];
    const next = deduped[i + 1];
    let type;
    if (!prev)      type = ev.height >= (next ? next.height : 0) ? "H" : "L";
    else if (!next) type = ev.height >= prev.height ? "H" : "L";
    else            type = (ev.height >= prev.height && ev.height >= next.height) ? "H" : "L";
    return { ...ev, type };
  });

  // Step 4: enforce strict H-L-H-L alternation — drop events that break the pattern,
  // keeping the more extreme one when two consecutive events have the same type.
  const alternated = [];
  for (const ev of labeled) {
    const last = alternated[alternated.length - 1];
    if (!last || last.type !== ev.type) {
      alternated.push(ev);
    } else {
      // Same type as previous — keep whichever is more extreme
      const prevMore = last.type === "H"
        ? last.height >= ev.height
        : last.height <= ev.height;
      if (!prevMore) alternated[alternated.length - 1] = ev;
    }
  }

  return alternated;
}

/** Compute sinusoidal current (kt) at time t (minutes from midnight of selected date).
 *  ebbFraction: fraction [0.5–0.7] at which the ebb-phase sine peaks; flood peaks at (1 – ebbFraction).
 *  Values >0.5 mean outgoing current persists longer before relaxing to the next slack. */
function currentAt(t, tideEvents, slackDelay, maxCurrent, wwFactor, ebbFraction = 0.55) {
  // Build slack times from tide peaks
  const slacks = tideEvents
    .map(ev => ({ time: ev.time + slackDelay, afterHigh: ev.type === "H" }))
    .sort((a, b) => a.time - b.time);

  if (slacks.length < 2) return wwFactor;

  let before = slacks[0], after = slacks[slacks.length - 1];
  for (let i = 0; i < slacks.length - 1; i++) {
    if (slacks[i].time <= t && slacks[i + 1].time > t) {
      before = slacks[i]; after = slacks[i + 1]; break;
    }
  }

  const interval = Math.max(after.time - before.time, 1);
  const phase    = (t - before.time) / interval; // linear 0→1 across interval

  // Asymmetric sine: remap phase so the peak occurs at ebbFraction (ebb) or (1-ebbFraction) (flood)
  const isEbb   = before.afterHigh;
  const peakAt  = isEbb ? ebbFraction : (1 - ebbFraction);
  const normPhase = phase < peakAt
    ? (phase / peakAt) * 0.5
    : 0.5 + ((phase - peakAt) / Math.max(1 - peakAt, 0.001)) * 0.5;

  const amp = maxCurrent * Math.sin(Math.PI * Math.max(0, Math.min(1, normPhase)));
  const dir = before.afterHigh ? 1 : -1;
  return parseFloat((amp * dir + wwFactor).toFixed(2));
}

/** Build 48-hour chart dataset at 10-min resolution */
function buildChartData(date, pass, wwFactor, adjustedMax, adjustedDelay) {
  const effMax      = (adjustedMax != null ? adjustedMax : pass.maxCurrent) * (pass.lagoonFactor != null ? pass.lagoonFactor : 1);
  const effDelay    = adjustedDelay != null ? adjustedDelay : pass.slackDelay;
  const ebbFraction = pass.ebbFraction != null ? pass.ebbFraction : 0.55;
  const tideEvents  = getExpandedTides(date, pass);
  const slacks = tideEvents
    .map(ev => ({ time: ev.time + effDelay, afterHigh: ev.type === "H", tide: ev }))
    .sort((a, b) => a.time - b.time)
    .filter(s => s.time >= -60 && s.time <= 48 * 60 + 60);

  const data = [];
  for (let t = 0; t <= 48 * 60; t += 10) {
    const c = currentAt(t, tideEvents, effDelay, effMax, wwFactor, ebbFraction);
    data.push({
      t,
      current: c,
      out: c > 0 ? c : 0,
      inn: c < 0 ? c : 0,
    });
  }
  return { data, slacks, tideEvents };
}

/** Find zero-crossing transit windows */
function findTransitWindows(data, slacks, pass, wwFactor, adjustedMax, boatSpeed) {
  const effMax = adjustedMax != null ? adjustedMax : pass.maxCurrent;
  // Safe transit threshold: boat needs ~2.5:1 speed-to-current ratio for control
  const safeKt = Math.max(0.5, (boatSpeed || 6) / 2.5);
  const windows = [];
  for (let i = 1; i < data.length; i++) {
    const a = data[i - 1], b = data[i];
    if (a.current * b.current > 0) continue;
    if (Math.abs(a.current) < 0.05 && Math.abs(b.current) < 0.05) continue;

    const frac = Math.abs(a.current) / (Math.abs(a.current) + Math.abs(b.current) + 0.001);
    const t = Math.round(a.t + 10 * frac);

    // Deduplicate: if current landed on exactly 0, both adjacent pairs fire — keep only first
    const prev = windows[windows.length - 1];
    if (prev && t - prev.t < 30) continue;

    // Direction after crossing
    const nearSlack = slacks.reduce((best, s) =>
      Math.abs(s.time - t) < Math.abs(best.time - t) ? s : best, slacks[0] || { time: t, afterHigh: true });

    // How many minutes stay within safe current threshold for this boat speed
    let windowMin = 0;
    for (let j = i; j < data.length; j++) {
      if (Math.abs(data[j].current) <= safeKt) windowMin += 10;
      else if (windowMin > 0) break;
    }

    // Quality uses adjusted max so spring/neap WW threshold scales correctly
    const quality = Math.abs(wwFactor) > effMax * 0.5 ? "poor"
                  : windowMin < 20                     ? "narrow"
                  : windowMin < 50                     ? "good"
                  : "ideal";

    // isFloodStart: ebb(+) → flood(-) crossing = flood is starting = current will push INTO lagoon
    const isFloodStart = a.current > b.current;
    windows.push({
      t, windowMin, quality, isFloodStart,
      direction: nearSlack.afterHigh ? "Outgoing \u2192 Incoming" : "Incoming \u2192 Outgoing",
    });
  }
  return windows.filter(w => w.t >= 0 && w.t <= 48 * 60);
}

/** Compute best single-day transit summary (quality + first best time) */
function buildDaySummary(date, pass, wwFactor, boatSpeed) {
  const { adjustedMax, adjustedDelay } = getTidalState(date, pass);
  const { data, slacks } = buildChartData(date, pass, wwFactor, adjustedMax, adjustedDelay);
  const wins = findTransitWindows(data, slacks, pass, wwFactor, adjustedMax, boatSpeed)
    .filter(w => w.t >= 0 && w.t < 1440); // today only
  if (!wins.length) return null;
  const order = ["ideal", "good", "narrow", "poor"];
  wins.sort((a, b) => order.indexOf(a.quality) - order.indexOf(b.quality));
  return { quality: wins[0].quality, time: wins[0].t, count: wins.length };
}

// ─── TIDAL STATE (SPRING / NEAP) ─────────────────────────────────────────────
const MEAN_RANGE_FT = 2.8; // mean tidal range at Rangiroa in feet

/** Get tidal range for a given date (high minus adjacent low) */
function getDailyRange(date) {
  const events = [];
  [0, 1].forEach(offset => {
    const raw = TIDE_DATA[toDateStr(shiftDate(date, offset))];
    if (!raw) return;
    raw.forEach(([tMin, h]) => events.push({ time: tMin + offset * 1440, height: h }));
  });
  if (events.length < 2) return MEAN_RANGE_FT;
  // Use max-minus-min over the two-day window as a robust range estimate
  const heights = events.map(e => e.height);
  return Math.max(...heights) - Math.min(...heights);
}

/** Compute adjusted current amplitude and slack delay based on spring/neap state */
function getTidalState(date, pass) {
  const range    = getDailyRange(date);
  const ratio    = range / MEAN_RANGE_FT;
  const sqrtRatio = Math.sqrt(Math.max(ratio, 0.4)); // clamp to avoid near-zero

  const age      = moonAge(new Date(date));
  // Spring: within 3.5 days of new (0) or full (14.77) moon
  const isSpring = age < 3.5 || age > (LUNATION - 3.5) || Math.abs(age - 14.77) < 3.5;
  // Neap: within 2.5 days of first (9.22) or last (24.0) quarter
  const isNeap   = Math.abs(age - 9.22) < 2.5 || Math.abs(age - 24.0) < 2.5;

  const springLabel = isSpring ? "Spring Tides" : isNeap ? "Neap Tides" : "Mid-Range Tides";

  const adjustedMax   = parseFloat((pass.maxCurrent * sqrtRatio).toFixed(2));
  const adjustedDelay = Math.round(pass.slackDelay / sqrtRatio);

  return { range, ratio, sqrtRatio, isSpring, isNeap, springLabel, adjustedMax, adjustedDelay };
}

// ─── FORMATTING ──────────────────────────────────────────────────────────────
// tzOff: 0 = TAHT (UTC−10, local), 600 = UTC
function toHHMM(tMin, tzOff = 0) {
  const t = ((Math.round(tMin + tzOff) % 1440) + 1440) % 1440;
  const h = Math.floor(t / 60), m = t % 60;
  const ampm = h < 12 ? "AM" : "PM";
  const h12  = h === 0 ? 12 : h > 12 ? h - 12 : h;
  return `${h12}:${String(m).padStart(2, "0")} ${ampm}`;
}

function toDayLabel(tMin, tzOff = 0) {
  const shifted = tMin + tzOff;
  const day = Math.floor(shifted / 1440);
  const prefix = day === 0 ? "Today" : day === 1 ? "Tomorrow" : day < 0 ? "Yesterday" : `+${day}d`;
  return `${prefix} ${toHHMM(tMin, tzOff)}`;
}

function toAxisLabel(t, tzOff = 0) {
  const hr = Math.round((t + tzOff) / 60) % 24;
  if (hr === 6)  return "6AM";
  if (hr === 12) return "Noon";
  if (hr === 18) return "6PM";
  if (hr === 0)  return "12AM";
  return "";
}

